#include <stdio.h>  // เปลี่ยนจาก stdio.h เป็น <stdio.h> 

int main() {        
    int a = 10;  // เปลี่ยนจาก int a = 10 -> int a = 10; // int ชื่อตัวแปร a กำหนดค่าเริ่มต้นเป็น 10
    printf("a = %d", a);  // เปลี่ยนจาก printf("a = %d", a) -> printf("a = %d", a); //
    return 0;       // เปลี่ยนจาก return 0 -> return 0; //
}